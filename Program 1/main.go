 package main

 import "fmt"

 func main() { 
	 //shows conversion
	 fmt.Println("What is 34 degrees Celcius in Fahrenheit?")
	 fmt.Println("34 degrees Celcius = " + fmt.Sprint(34*9/5+32) + " F")

	 fmt.Println("\nDone.")
}